const mongoose = require('mongoose')

const mediaSchema = new mongoose.Schema({
  mainPicture: String,
  videoUpload: String,
  mapImage: String,
  noc: String,
  registry: String,
  legalDoc: String,
  moreImages: [String]
}, { _id: false })

const propertySchema = new mongoose.Schema({
  name: {
    type: String,
    required: [true, 'Property name is required'],
    trim: true
  },
  category: {
    type: String,
    enum: ['Residential', 'Commercial', 'Farmhouse'],
    default: 'Residential'
  },
  colony: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Colony',
    required: [true, 'Colony reference is required']
  },
  city: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'City'
  },
  area: {
    type: String,
    trim: true
  },
  address: {
    type: String,
    trim: true
  },
  tagline: {
    type: String,
    trim: true
  },
  description: {
    type: String,
    trim: true
  },
  facilities: [{
    type: String
  }],
  amenities: [{
    type: String
  }],
  media: {
    type: mediaSchema,
    default: () => ({ moreImages: [] })
  },
  status: {
    type: String,
    enum: ['draft', 'active', 'inactive'],
    default: 'active'
  },
  createdBy: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  }
}, {
  timestamps: true
})

module.exports = mongoose.model('Property', propertySchema)
